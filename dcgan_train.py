import pandas as pd
import cv2
import numpy as np
from sklearn.model_selection import train_test_split

csv_path = '/mnt/c/123/HAM10000_metadata.csv'
img_dir = '/mnt/c/123/data/images'

df = pd.read_csv(csv_path)

# Binary labels: malignant=1, benign=0
malignant = ['mel', 'bcc', 'akiec']
df['label'] = df['dx'].apply(lambda x: 1 if x in malignant else 0)

IMG_SIZE = 64
X_real, y_real = [], []

for _, row in df.iterrows():
    img_path = img_dir + '/' + row['image_id'] + '.jpg'
    if not os.path.exists(img_path):
        continue

    img = cv2.imread(img_path)
    img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
    img = cv2.resize(img, (IMG_SIZE, IMG_SIZE))
    img = img / 255.0

    X_real.append(img)
    y_real.append(row['label'])

X_real = np.array(X_real)
y_real = np.array(y_real)

print("Real images:", X_real.shape)

from tensorflow.keras.models import load_model
import tensorflow as tf

generator = load_model('/mnt/c/123/dcgan_generator_epoch50.keras')

NUM_SYNTH = len(X_real) // 2   # 50% augmentation
noise = tf.random.normal([NUM_SYNTH, 100])

X_synth = generator(noise, training=False)
X_synth = (X_synth + 1) / 2.0

# Assign labels (same distribution)
y_synth = np.random.choice([0,1], size=len(X_synth))

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import Conv2D, MaxPooling2D, Flatten, Dense, Dropout

def build_classifier():
    model = Sequential([
        Conv2D(32, 3, activation='relu', input_shape=(64,64,3)),
        MaxPooling2D(),
        Conv2D(64, 3, activation='relu'),
        MaxPooling2D(),
        Flatten(),
        Dense(128, activation='relu'),
        Dropout(0.5),
        Dense(1, activation='sigmoid')
    ])

    model.compile(
        optimizer='adam',
        loss='binary_crossentropy',
        metrics=['accuracy']
    )
    return model

X_train, X_test, y_train, y_test = train_test_split(
    X_real, y_real, test_size=0.2, random_state=42
)

model_real = build_classifier()
history_real = model_real.fit(
    X_train, y_train,
    epochs=10,
    batch_size=32,
    validation_data=(X_test, y_test),
    verbose=1
)

real_acc = history_real.history['val_accuracy'][-1]
print("Validation Accuracy (Real only):", real_acc)

